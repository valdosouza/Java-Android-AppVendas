package com.setes.setesvendas.app.controller;


public class EtdProduct
{

    String created_at;
    String description;
    String enabled;
    String highlights;
    int id;
    String identifier;
    String note;
    String promotion;
    String published;
    int tb_category_id;
    int tb_institution_id;
    String updated_at;

    public EtdProduct(){

    }
    public EtdProduct(String created_at, String description, String enabled, String highlights, int id, String identifier, String note, String promotion, String published, int tb_category_id, int tb_institution_id, String updated_at) {
        this.created_at = created_at;
        this.description = description;
        this.enabled = enabled;
        this.highlights = highlights;
        this.id = id;
        this.identifier = identifier;
        this.note = note;
        this.promotion = promotion;
        this.published = published;
        this.tb_category_id = tb_category_id;
        this.tb_institution_id = tb_institution_id;
        this.updated_at = updated_at;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getEnabled() {
        return enabled;
    }

    public void setEnabled(String enabled) {
        this.enabled = enabled;
    }

    public String getHighlights() {
        return highlights;
    }

    public void setHighlights(String highlights) {
        this.highlights = highlights;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIdentifier() {
        return identifier;
    }

    public void setIdentifier(String identifier) {
        this.identifier = identifier;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getPromotion() {
        return promotion;
    }

    public void setPromotion(String promotion) {
        this.promotion = promotion;
    }

    public String getPublished() {
        return published;
    }

    public void setPublished(String published) {
        this.published = published;
    }

    public int getTb_category_id() {
        return tb_category_id;
    }

    public void setTb_category_id(int tb_category_id) {
        this.tb_category_id = tb_category_id;
    }

    public int getTb_institution_id() {
        return tb_institution_id;
    }

    public void setTb_institution_id(int tb_institution_id) {
        this.tb_institution_id = tb_institution_id;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }
}
