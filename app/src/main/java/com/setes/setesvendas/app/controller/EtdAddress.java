package com.setes.setesvendas.app.controller;


public class EtdAddress {

    String complement;
    String created_at;
    int id;
    String kind;
    String main;
    String neighborhood;
    String nmbr;
    String region;
    String street;
    int tb_city_id;
    int tb_country_id;
    int tb_state_id;
    String updated_at;
    String zip_code;

    public EtdAddress()
    {

    }
    public EtdAddress(String complement, String created_at, int id, String kind, String main, String neighborhood, String nmbr, String region, String street, int tb_city_id, int tb_country_id, int tb_state_id, String updated_at, String zip_code) {
        this.complement = complement;
        this.created_at = created_at;
        this.id = id;
        this.kind = kind;
        this.main = main;
        this.neighborhood = neighborhood;
        this.nmbr = nmbr;
        this.region = region;
        this.street = street;
        this.tb_city_id = tb_city_id;
        this.tb_country_id = tb_country_id;
        this.tb_state_id = tb_state_id;
        this.updated_at = updated_at;
        this.zip_code = zip_code;
    }

    public String getComplement() {
        return complement;
    }

    public void setComplement(String complement) {
        this.complement = complement;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getMain() {
        return main;
    }

    public void setMain(String main) {
        this.main = main;
    }

    public String getNeighborhood() {
        return neighborhood;
    }

    public void setNeighborhood(String neighborhood) {
        this.neighborhood = neighborhood;
    }

    public String getNmbr() {
        return nmbr;
    }

    public void setNmbr(String nmbr) {
        this.nmbr = nmbr;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public int getTb_city_id() {
        return tb_city_id;
    }

    public void setTb_city_id(int tb_city_id) {
        this.tb_city_id = tb_city_id;
    }

    public int getTb_country_id() {
        return tb_country_id;
    }

    public void setTb_country_id(int tb_country_id) {
        this.tb_country_id = tb_country_id;
    }

    public int getTb_state_id() {
        return tb_state_id;
    }

    public void setTb_state_id(int tb_state_id) {
        this.tb_state_id = tb_state_id;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }

    public String getZip_code() {
        return zip_code;
    }

    public void setZip_code(String zip_code) {
        this.zip_code = zip_code;
    }
}

