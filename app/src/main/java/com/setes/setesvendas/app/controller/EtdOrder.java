package com.setes.setesvendas.app.controller;


public class EtdOrder {

    String created_at;
    String dt_record;
    int id;
    String note;
    String status;
    int tb_institution_id;
    String updated_at;

    public EtdOrder() {

    }

    public EtdOrder(String created_at, String dt_record, int id, String note, String status, int tb_institution_id, String updated_at) {
        this.created_at = created_at;
        this.dt_record = dt_record;
        this.id = id;
        this.note = note;
        this.status = status;
        this.tb_institution_id = tb_institution_id;
        this.updated_at = updated_at;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public String getDt_record() {
        return dt_record;
    }

    public void setDt_record(String dt_record) {
        this.dt_record = dt_record;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getTb_institution_id() {
        return tb_institution_id;
    }

    public void setTb_institution_id(int tb_institution_id) {
        this.tb_institution_id = tb_institution_id;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }
}
